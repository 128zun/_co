// 計算 R2 = R0 * R1
// 假設 R0 >= 0, R1 >= 0, 且結果 < 32768

    @R2
    M=0         // 初始化結果 R2 = 0

    @R1
    D=M
    @END
    D;JEQ       // 如果 R1 == 0，直接結束 (結果為 0)

(LOOP)
    @R0
    D=M         // 取出 R0 的值
    @R2
    M=D+M       // R2 = R2 + R0

    @R1
    M=M-1       // R1 = R1 - 1 (計數器減 1)
    D=M

    @LOOP
    D;JGT       // 如果 R1 > 0，繼續循環

(END)
    @END
    0;JMP       // 無限循環以結束程式